/**
 * Calculator module - See {@tutorial calculator-tutorials}
 * @module calculator
 */

/**
 * Add two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Sum of n1 and n2
 */
export function add(n1, n2) {
  return n1 + n2
}

/**
 * Multiply two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Product of n1 and n2
 */
export function multiply(n1, n2) {
  return n1 * n2
}

/**
 * Subtract two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Difference of n1 and n2
 */
export function subtract(n1, n2) {
  return n1 - n2
}

/**
 * Divide two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @returns {number} - Quotient of n1 and n2
 */
export function divide(n1, n2) {
  return n1 / n2
}
